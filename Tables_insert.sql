DROP TABLE IF EXISTS Action_en_justice;
DROP TABLE IF EXISTS Reunion;
DROP TABLE IF EXISTS Facture;
DROP TABLE IF EXISTS Affaire;
DROP TABLE IF EXISTS Avocat;
DROP TABLE IF EXISTS Cabinet;
DROP TABLE IF EXISTS Entreprise;
DROP TABLE IF EXISTS Personne;
DROP TABLE IF EXISTS Client;

-- Affaire Table
CREATE TABLE Affaire (
    ID_Affaire INT PRIMARY KEY,
    Titre VARCHAR(20),
    Categorie_juridique VARCHAR(20),
    Date_ouverture DATE NOT NULL,
    Date_cloture DATE,
    Statut VARCHAR(20) DEFAULT 'en cours',
    ID_Avocat INT,
    ID_Client INT,
    CONSTRAINT fk_affaire_avocat FOREIGN KEY (ID_Avocat) REFERENCES Avocat(ID_Avocat),
    CONSTRAINT fk_affaire_client FOREIGN KEY (ID_Client) REFERENCES Client(ID_Client),
    CONSTRAINT chk_date_cloture_sup_date_ouverture CHECK (Date_cloture > Date_ouverture)
);

-- Facture Table
CREATE TABLE Facture (
    ID_Facture INT PRIMARY KEY,
    Montant DECIMAL(10, 2) NOT NULL,
    Date_emission DATE NOT NULL,
    Date_echeance DATE,
    Mode_paiement VARCHAR(20),
    ID_Affaire INT,
    ID_Client INT,
    ID_Cabinet INT,
    CONSTRAINT fk_facture_affaire FOREIGN KEY (ID_Affaire) REFERENCES Affaire(ID_Affaire),
    CONSTRAINT fk_facture_client FOREIGN KEY (ID_Client) REFERENCES Client(ID_Client),
    CONSTRAINT fk_facture_cabinet FOREIGN KEY (ID_Cabinet) REFERENCES Cabinet(ID_Cabinet),
    CONSTRAINT chk_date_echeance_sup_date_emission CHECK (Date_echeance > Date_emission)
);

-- Avocat Table
CREATE TABLE Avocat (
    ID_Avocat INT PRIMARY KEY,
    Nom VARCHAR(20) NOT NULL,
    Prenom VARCHAR(20) NOT NULL,
    Date_naissance DATE,
    Genre VARCHAR(10),
    Adresse VARCHAR(20),
    Ville VARCHAR(20),
    Tel VARCHAR(15),
    Email VARCHAR(20) NOT NULL,
    Num_barreau VARCHAR(20) UNIQUE,
    Specialite VARCHAR(20),
    ID_Cabinet INT,
    CONSTRAINT fk_avocat_cabinet FOREIGN KEY (ID_Cabinet) REFERENCES Cabinet(ID_Cabinet)
);

-- Cabinet Table
CREATE TABLE Cabinet (
    ID_Cabinet INT PRIMARY KEY,
    Nom VARCHAR(20) NOT NULL,
    Adresse VARCHAR(20) NOT NULL,
    Ville VARCHAR(20) NOT NULL,
    Date_creation DATE
);

-- Reunion Table
CREATE TABLE Reunion (
    ID_Reunion INT PRIMARY KEY,
    Date DATE NOT NULL,
    Heure_debut TIME NOT NULL,
    Heure_fin TIME NOT NULL,
    Lieu VARCHAR(20) NOT NULL,
    ID_Affaire INT,
    CONSTRAINT fk_reunion_affaire FOREIGN KEY (ID_Affaire) REFERENCES Affaire(ID_Affaire),
    CONSTRAINT chk_heure_debut_sup_heure_fin CHECK (Heure_debut < Heure_fin)
);

-- Action_en_justice Table
CREATE TABLE Action_en_justice (
    ID_Action INT PRIMARY KEY,
    Descriptions VARCHAR(20),
    Statut VARCHAR(20) NOT NULL,
    Date_action DATE NOT NULL,
    Lieu VARCHAR(20),
    Heure_debut TIME,
    ID_Affaire INT,
    CONSTRAINT fk_action_affaire FOREIGN KEY (ID_Affaire) REFERENCES Affaire(ID_Affaire)
);

-- Client Table
CREATE TABLE Client (
    ID_Client INT PRIMARY KEY,
    Type_client VARCHAR(20) NOT NULL
);

-- Personne Table
CREATE TABLE Personne (
    ID_Personne INT PRIMARY KEY,
    Nom VARCHAR(20) NOT NULL,
    Prenom VARCHAR(20) NOT NULL,
    Date_naissance DATE CHECK (Date_naissance <= CURRENT_DATE - INTERVAL 18 YEAR),
    Genre VARCHAR(10),
    Numero_secu VARCHAR(20) UNIQUE,
    Adresse VARCHAR(20),
    Ville VARCHAR(20),
    Tel VARCHAR(15),
    Email VARCHAR(20) NOT NULL,
    ID_Client INT,
    CONSTRAINT fk_personne_client FOREIGN KEY (ID_Client) REFERENCES Client(ID_Client)
);

-- Entreprise Table
CREATE TABLE Entreprise (
    ID_Entreprise INT PRIMARY KEY,
    Siret VARCHAR(20) UNIQUE NOT NULL,
    Nbr_employes INT,
    Adresse VARCHAR(20),
    Ville VARCHAR(20),
    Secteur VARCHAR(20),
    Email VARCHAR(20) NOT NULL,
    ID_Client INT,
    CONSTRAINT fk_entreprise_client FOREIGN KEY (ID_Client) REFERENCES Client(ID_Client)
);


